CREATE TABLE IF NOT EXISTS sprites (
    pokemon_id INT,
    official_artwork VARCHAR(256),
    official_artwork_shiny VARCHAR(256),
    back_default VARCHAR(256),
    back_female VARCHAR(256),
    front_default VARCHAR(256),
    front_female VARCHAR(256),
    back_shiny VARCHAR(256),
    back_shiny_female VARCHAR(256),
    front_shiny VARCHAR(256),
    front_shiny_female VARCHAR(256)
);